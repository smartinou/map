//! \brief Non-Volatile Memory interface.
class INVMem {
public:
    virtual unsigned int GetNVMemSize(void) const = 0;
    virtual void RdFromNVMem(
        uint8_t * const aDataPtr,
        std::size_t aOffset,
        std::size_t aSize
    ) = 0;
    virtual void WrToNVMem(
        uint8_t const * const aDataPtr,
        std::size_t aOffset,
        std::size_t aSize
    ) = 0;
};

