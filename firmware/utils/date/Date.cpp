std::string Date::ToStr(void) const {

    static size_t constexpr sStrLen = 16;
    char lDateStr[sStrLen]{'\0'};
    snprintf(
        &lDateStr[0],
        sStrLen,
        "%04d-%02d-%02d",
        GetYear(),
        GetMonth(),
        GetDate()
    );

    return std::string(lDateStr);
}


std::string Date::ToStr2(void) const {

    static size_t constexpr sStrLen = 16;
    char lDateStr[sStrLen]{'\0'};
    auto const lMonthStr = mMonth.ToStr();
    snprintf(
        &lDateStr[0],
        sStrLen,
        "%04d-%s-%02d",
        GetYear(),
        lMonthStr.data(),
        GetDate()
    );

    return std::string(lDateStr);
}

